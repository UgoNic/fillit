int	ft_errordot(char *s)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		if ((i + 1) % 5 == 0 && s[i] != '\n')
			return (0);
		i++;
	}
	return (1);
}
#include <stdio.h>
int main()
{
	char *s = "...#\n...#\n.#..\n...#\n";

	printf("%s\n%d\n", s, ft_errordot(s));
	return 0;
}
