#include <stdio.h>
void	upleft(char *s)
{
	int	i;
	int	y;

	i = 0;
	while (s[i] != '#')
		i++;
	y = i;
	while (s[i])
	{
		if (s[i] == '#')
		{
			s[i] = '.';
			s[i - y] = '#';
		}
		i++;
	}
}

int main()
{
	char	s[30] = "..##\n...#\n...#\n....\n";
	printf("%s\n", s);
	upleft(s);
	printf("%s\n", s);
	return 0;
}

